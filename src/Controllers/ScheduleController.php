<?php

namespace App\Controllers;

use App\Entities\Database\Object\BaseObjectEntity;
use App\Enums\ActionConstructors\EScheduleConstructor;
use App\Models\Database\ScheduleDatabase;
use App\Models\Database\UserToScheduleDatabase;
use App\Models\Facade\ScheduleFacade;
use App\Utilities\RedirectUtils;

class ScheduleController extends BaseController
{
	public const VIEW_GRID = "src/Views/Schedule/grid.latte";
	public const VIEW_EDIT = "src/Views/Schedule/edit.latte";
	protected string $controllerName = EScheduleConstructor::CONTROLLER_NAME;
	public function __construct($latte)
	{
		parent::__construct($latte);
		$this->scheduleFacade = new ScheduleFacade(new ScheduleDatabase(), new UserToScheduleDatabase());
	}

	public function show(string $path = self::VIEW_EDIT, $args = [])
	{
		if ($path === self::VIEW_EDIT){
			$args['selectHobbyGroups'] = $this->scheduleFacade->hobbygroupFacade->getAllHobbyGroups();
			$args['selectSchoolrooms'] = $this->scheduleFacade->schoolroomFacade->getAllSchoolrooms();
		}
		return parent::show($path, $args); // TODO: Change the autogenerated stub
	}

	public function actionGrid()
	{
		$gridValues = $this->scheduleFacade->getAllSchedules();
		$args["gridvalues"] = $gridValues;
		$this->show(self::VIEW_GRID, $args);
	}

	public function saveForm($variables)
	{
		return $this->scheduleFacade->saveSchedule($variables);
	}

	public function loadForm($variables)
	{
		$args['formData'] = $this->scheduleFacade->getFullSchedule($variables[BaseObjectEntity::BASE_ID]);
		$this->show(self::VIEW_EDIT, $args);
	}

	public function redirectEdit($id)
	{
		RedirectUtils::redirectWithURLBuild(EScheduleConstructor::edit(), [BaseObjectEntity::BASE_ID => $id]);
	}

	public function actionEdit()
	{
		parent::actionEdit(); // TODO: Change the autogenerated stub
	}
}