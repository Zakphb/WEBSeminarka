<?php

namespace App\Entities\Database\Object;

class SchoolroomTypeObjectEntity extends BaseObjectEntity
{
	const SCHOOLROOM_TYPE_NAME = 'name';

	protected $name;

	/**
	 * @return int|null
	 */
	public function getId(): ?int
	{
		return $this->id;
	}

	/**
	 * @param int|null $id
	 */
	public function setId(?int $id): void
	{
		$this->id = $id;
	}

	/**
	 * @return mixed
	 */
	public function getName()
	{
		return $this->name;
	}

	/**
	 * @param mixed $name
	 */
	public function setName($name): void
	{
		$this->name = $name;
	}
}